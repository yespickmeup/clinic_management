    private void init_report() {
        Button.Search search = (Button.Search) jButton3;
        search.load();
        Thread t = new Thread(new Runnable() {
            @Override
            public void run() {
                List<Clinics.to_clinics> clinics = Clinics.ret_data("where id='" + to.clinic_id + "'");

                String clinic_name = "";
                String clinic_address = "";
                String clinic_contact_no = "";
                if (!clinics.isEmpty()) {
                    Clinics.to_clinics clinic = (Clinics.to_clinics) clinics.get(0);
                    clinic_name = clinic.clinic;
                    clinic_address = clinic.address;
                    clinic_contact_no = clinic.contact_no;
                }
                String patient_id = to.patient_id;
                String patient_name = to.patient;
                String bday = to.patient_bday;
                String age = "";
                String gender = "" + to.patient_gender;
                String allergy_drug = "" + to.patient_allergy_medicine;
                String allergy_others = "" + to.patient_allergy_others;
                String blood_group = to.patient_blood_type;
                String height = "" + to.patient_height;
                String weight = "" + to.patient_weight;
                String temperature = "" + to.patient_temperature;
                String bp = "" + to.patient_pressure;
                String pulse = "" + to.patient_pulse;
                String respiratory = "" + to.patient_respiratory;
                String spo2 = "" + to.patient_spo2;
                String is_smoking = "" + to.patient_is_smoking;
                String is_alcohol = "" + to.patient_is_alcohol;
                String is_drugs = "" + to.patient_is_drug_abuse;
                String complaints = "" + to.complaints;

                String history = "" + to.past_personal_family_history;
                String investigation = to.investigation;
                List<Srpt_case_report.field> fields = new ArrayList();
               
                Srpt_case_report rpt = new Srpt_case_report(clinic_name, clinic_address, clinic_contact_no, patient_id, patient_name, bday, age, gender, allergy_drug, allergy_others, blood_group, height, weight, temperature, bp, pulse, respiratory, spo2, is_smoking, is_alcohol, is_drugs, complaints, history, investigation);
                rpt.fields.addAll(fields);
                String jrxml = "rpt_investigation_report.jrxml";
                report_sales_items(rpt, jrxml);

                InputStream is = Srpt_case_report.class.getResourceAsStream(jrxml);
                try {
                    JasperReport jasperReport = JasperCompileManager.compileReport(is);
                    if (rpt.fields.isEmpty()) {
                        jasperPrint = JasperFillManager.fillReport(jasperReport, JasperUtil.
                                setParameter(rpt), JasperUtil.emptyDatasource());
                    } else {
                        jasperPrint = JasperFillManager.fillReport(jasperReport, JasperUtil.
                                setParameter(rpt), JasperUtil.makeDatasource(rpt.fields));
                    }

                } catch (JRException ex) {
                    Logger.getLogger(Dlg_report_investigation_report.class.getName()).
                            log(Level.SEVERE, null, ex);
                }
                Button.Search search = (Button.Search) jButton3;
                search.finish();
            }
        });
        t.start();

    }

    private void report_sales_items(final Srpt_case_report to, String jrxml_name) {
        pnl_report.removeAll();
        pnl_report.setLayout(new BorderLayout());
        try {
            JRViewer viewer = get_viewer_expenses(to, jrxml_name);
            JPanel pnl = new JPanel();
            pnl.add(viewer);
            pnl.setVisible(true);
            pnl.setVisible(true);
            pnl_report.add(viewer);
            pnl_report.updateUI();

        } catch (Exception e) {
            throw new RuntimeException(e);
        }
    }

    public static JRViewer get_viewer_expenses(Srpt_case_report to, String rpt_name) {
        try {
            if (to.fields.isEmpty()) {
                return JasperUtil.getJasperViewer(
                        compileJasper(rpt_name),
                        JasperUtil.setParameter(to),
                        JasperUtil.emptyDatasource());
            } else {
                return JasperUtil.getJasperViewer(
                        compileJasper(rpt_name),
                        JasperUtil.setParameter(to),
                        JasperUtil.makeDatasource(to.fields));
            }

        } catch (Exception e) {
            throw new RuntimeException(e);
        } finally {
        }
    }

    public static JasperReport compileJasper(String rpt_name) {
        try {
            String jrxml = rpt_name;
            InputStream is = Srpt_case_report.class.getResourceAsStream(jrxml);
            JasperReport jasper = JasperCompileManager.compileReport(is);
            return jasper;
        } catch (JRException e) {
            throw new RuntimeException(e);
        }
    }
    JasperPrint jasperPrint = null;

    private void print() {
        try {
            if (jasperPrint != null) {
                JasperPrintManager.printReport(jasperPrint, false);
            }

        } catch (JRException e) {
            JOptionPane.showMessageDialog(null, "Failed To Print, Please Check the Printer");
            throw new RuntimeException(e);
        }
    }
