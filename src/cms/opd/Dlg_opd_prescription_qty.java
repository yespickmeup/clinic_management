    private void ok() {
        double qty = FitIn.toDouble(jTextField2.getText());
        String dosage = jTextField5.getText();
        int days = FitIn.toInt(jTextField6.getText());
        double dosage_qty = FitIn.toDouble(jTextField7.getText());
        String dosage_remarks = jTextField8.getText();
        String remarks = jTextField9.getText();
        if (qty <= 0) {
            Alert.set(0, "Enter quantity!");
            jTextField2.grabFocus();
            return;
        }
        if (dosage_qty <= 0) {
            Alert.set(0, "Enter quantity!");
            jTextField7.grabFocus();
            return;
        }
        if (callback != null) {
            callback.ok(new CloseDialog(this), new OutputData(qty, dosage, days, dosage_qty, dosage_remarks, remarks));
        }
    }
