    private void ok() {
        double discount=FitIn.toDouble(jTextField5.getText());
        if(discount<0){
            Alert.set(0,"Input discount!");
            jTextField5.grabFocus();
            return;
        }
        if (callback != null) {
            callback.ok(new CloseDialog(this), new OutputData(discount));
        }
    }
