    private void ok() {
        String complaint=jTextField26.getText();
        if(complaint.isEmpty()){
            Alert.set(0, "Input field!");
            jTextField26.grabFocus();
            return;
        }
        if (callback != null) {
            callback.ok(new CloseDialog(this), new OutputData(complaint));
        }
    }
